x = 5
y = 2
print(x + y)  # Addition
print(x - y)  # Subtraction

print(x * y)  # Multiplication
print(x / y)  # Division
print(x % y)  # Modulus
print(x ** y)  # Exponentiation
print(x // y)  # Floor Division


print(x > y)  # Greater than
print(x < y)  # Less than

print(x == y)  # Equal to
print(x != y)  # Not equal to
print(x > 3 and y < 3)  # Logical AND
print(x > 3 or y < 3)  # Logical OR

print(x << y)  # Less than
print(x >> y)  # Greater than